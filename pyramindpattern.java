// PRINT INVERTED PIRAMIND

 import java.util.*;
  public class recursion{
     public static void main(String args[]){
         int n=5;
         for(int i=0;i<=n;i++){
             for(int i=0;i<=n-i;i++){
             System.out.print(" ");
             System.out.print("*");
         }
        
        
         System.out.println();
     }
 } 
}
